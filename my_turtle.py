import turtle
my_turtle=turtle.Turtle()
def square():
    my_turtle.forward(100)
    my_turtle.left(90)
    my_turtle.forward(100)
    my_turtle.left(90)
    my_turtle.forward(100)
    my_turtle.left(90)
    my_turtle.forward(100)

def rectangle():
    my_turtle.forward(150)
    my_turtle.left(90)
    my_turtle.forward(100)
    my_turtle.left(90)
    my_turtle.forward(150)
    my_turtle.left(90)
    my_turtle.forward(100)
    my_turtle.left(90)

def small():
    my_turtle.forward(50)
    my_turtle.left(90)
    my_turtle.forward(50)
    my_turtle.left(90)
    my_turtle.forward(25)
    my_turtle.left(90)
    my_turtle.forward(50)
square()
rectangle()
small()

    
